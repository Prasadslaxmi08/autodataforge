"""Phase-5 benchmark suite (reusable).

Generates several deterministic dataset profiles, runs the full deterministic
pipeline over each, and writes the baseline deliverables to benchmarks/:

    baseline_report.md   aggregate performance + quality + failure analysis
    comparison.md        the comparison-framework table (baseline registered)

Run:  python scripts/benchmark_suite.py
Datasets, raw JSON, and per-run reports land under benchmarks/ (git-ignored);
the two curated .md files above are the committed deliverables.
"""

from __future__ import annotations

import random
import shutil
from pathlib import Path

from PIL import Image, ImageDraw

from vds.comparison import ComparisonRegistry
from vds.container import Container
from vds.core.contracts import ExecutionReport
from vds.reporting import to_kpis

ROOT = Path("benchmarks")
DATASETS = ROOT / "datasets"

# (name, n_images, objects_per_image_range, object_size_range, resolutions)
PROFILES = [
    ("small", 10, (1, 3), (12, 28), [(96, 96), (128, 128)]),
    ("medium", 50, (2, 5), (12, 30), [(128, 128), (160, 120)]),
    ("large", 150, (2, 6), (14, 32), [(128, 128), (160, 160), (200, 150)]),
    ("mixed_classes", 30, (2, 5), (10, 40), [(128, 128), (180, 140), (96, 160)]),
    ("small_objects", 30, (3, 6), (4, 9), [(128, 128), (160, 160)]),
    ("dense_objects", 20, (9, 15), (8, 18), [(160, 160), (200, 200)]),
]


def generate(profile) -> Path:
    name, n, obj_range, size_range, resolutions = profile
    out = DATASETS / name
    if out.exists():
        shutil.rmtree(out)
    out.mkdir(parents=True)
    rng = random.Random(sum(ord(c) for c in name))  # stable across processes
    for i in range(n):
        w, h = resolutions[i % len(resolutions)]
        im = Image.new("RGB", (w, h), (240, 240, 240))
        dr = ImageDraw.Draw(im)
        for _ in range(rng.randint(*obj_range)):
            s = rng.randint(*size_range)
            x = rng.randint(2, max(3, w - s - 2))
            y = rng.randint(2, max(3, h - s - 2))
            shade = rng.randint(0, 60)
            dr.rectangle([x, y, x + s, y + s], fill=(shade, shade, shade))
        im.save(out / f"{name}_{i:03d}.png")
    return out


def aggregate_report(reports: dict[str, ExecutionReport]) -> str:
    lines = [
        "# Baseline Benchmark Report — Deterministic Pipeline",
        "",
        "Generated by `scripts/benchmark_suite.py` over the standard dataset profiles.",
        "All numbers are measured on the CPU `builtin` backend. Backend swap "
        "(YOLO/foundation models) is future work.",
        "",
        "## Per-profile summary",
        "",
        "| Profile | Images | Detections | img/s | avg conf | approve | review | reject "
        "| density | peak RAM MB |",
        "|---|---|---|---|---|---|---|---|---|---|",
    ]
    for name, r in reports.items():
        q, b = r.quality, r.benchmark
        lines.append(
            f"| {name} | {r.imported} | {r.detections} | {b.images_per_second} | "
            f"{q.avg_confidence} | {q.approval_rate:.0%} | {q.review_rate:.0%} | "
            f"{q.rejection_rate:.0%} | {q.annotation_density} | {b.peak_ram_mb} |"
        )

    lines += ["", "## Failure analysis across profiles (heuristic proxies)", "",
              "| Profile | low_conf | small_obj | duplicates | empty_mask | crowded | zero_det |",
              "|---|---|---|---|---|---|---|"]
    for name, r in reports.items():
        c = {cat.name: cat.count for cat in r.errors.categories}
        lines.append(
            f"| {name} | {c['low_confidence']} | {c['small_objects']} | "
            f"{c['duplicate_annotations']} | {c['empty_masks']} | {c['crowded_scenes']} | "
            f"{c['possible_missed_detection']} |"
        )

    tot_imgs = sum(r.imported for r in reports.values())
    tot_det = sum(r.detections for r in reports.values())
    ips = [r.benchmark.images_per_second for r in reports.values()]
    lines += [
        "",
        "## Executive Summary",
        f"- profiles run: {len(reports)}  ·  total images: {tot_imgs}  "
        f"·  total detections: {tot_det}",
        f"- throughput range: {min(ips)}–{max(ips)} img/s",
        "- not measurable without ground truth: "
        + "; ".join(next(iter(reports.values())).errors.unmeasurable),
        "",
    ]
    return "\n".join(lines)


def main() -> None:
    container = Container(db_path="benchmarks/suite.db")
    reports: dict[str, ExecutionReport] = {}
    for profile in PROFILES:
        name = profile[0]
        data = generate(profile)
        print(f"running profile: {name} ...")
        reports[name] = container.pipeline.run(
            str(data), name=name, export_format="coco", dest=str(DATASETS / name / "export")
        )

    (ROOT / "baseline_report.md").write_text(aggregate_report(reports), encoding="utf-8")

    # Register the aggregate as the deterministic baseline for future comparison.
    registry = ComparisonRegistry()
    medium = reports.get("medium") or next(iter(reports.values()))
    registry.register(to_kpis(medium, stage="deterministic"))
    (ROOT / "comparison.md").write_text(
        "# Comparison Framework\n\n"
        "KPIs per pipeline generation. Only the deterministic baseline exists "
        "today; Planner/Analyst/feedback/production rows are filled by later "
        "phases via `ComparisonRegistry.register(...)`.\n\n"
        + registry.render_table()
        + "\n",
        encoding="utf-8",
    )
    print("wrote benchmarks/baseline_report.md and benchmarks/comparison.md")


if __name__ == "__main__":
    main()
